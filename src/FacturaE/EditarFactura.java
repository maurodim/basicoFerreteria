    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FacturableE feB=new FacturaElectronica();
        if(fac.getIdCliente() > 0 && fac.getCae() != null){
            //fac.setCaeVto(fac.getFechaCae());
            //int cantt=fac.getCaeVto().length();
            fac.setFechaCae(fac.getCaeVto()+"  ");
            fac.setAfipQty(fac.getAfipPlastId());
            fac.setIdFactura(Integer.parseInt(numeroComprobante));
            fac.setEstado(1);
            feB.guardar(fac);
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    private void CargarTabla2(){
       String texto="<html>Cliente: "+fac.getIdCliente()+"<br> Tipo de Documento: "+fac.getCustomerTypeDoc()+"<br> Numero: "+fac.getCustomerId()+"<br> Numero FC: "+fac.getAfipPlastCbte()+"<br> Total Factura :"+fac.getImporteTotal()+"<br> CAE: "+fac.getCae()+"</html>";
        this.jLabel3.setText(texto);
        modelo2=new DefaultTableModel();
        modelo2.addColumn("");
        modelo2.addColumn("");
        Object[] fila=new Object[2]; 
        
       
        Iterator itD=detalleCargado.listIterator();
        DetalleFacturas dd;
        while(itD.hasNext()){
            dd=(DetalleFacturas) itD.next();
            fila[0]=dd.getDescripcionArticulo();
            fila[1]="$ "+dd.getPrecioUnitario();
            modelo2.addRow(fila);
        }
        
        this.jTable2.setModel(modelo2);
    }
    private void CargarTabla1(){
        
        Facturable facD=new DetalleFacturas();
        listadoDetalles=facD.listarPorEstado(0,0);

        modelo=facD.mostrarListado(listadoDetalles);
        this.jTable1.setModel(modelo);
    }

