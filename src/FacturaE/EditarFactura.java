    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int posicion=this.jTable1.getSelectedRow();
        detalle=(DetalleFacturas) listadoDetalles.get(posicion);
        detalle.setIdFactura(Integer.parseInt(numeroComprobante));
        Facturable factB=new DetalleFacturas();
        factB.modificarFactura(detalle);
        CargarTabla1();
        detalleCargado.add(detalle);
        CargarTabla2();
    }//GEN-LAST:event_jTable1MouseClicked

