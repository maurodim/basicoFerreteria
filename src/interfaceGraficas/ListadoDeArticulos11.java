    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList seleccionados=new ArrayList();
        Articulos articulo;
        Articulable aar=new Articulos();
        for(int a=0;a < jTable1.getRowCount();a++){
            if(tipo==0){
                if((Boolean)jTable1.getValueAt(a, 0)){
                    articulo=(Articulos) lstArticulos.get(a);
                    seleccionados.add(articulo);
                }
            }else{
                if((Boolean)jTable1.getValueAt(a, 0)){
                }else{
                articulo=(Articulos) lstArticulos.get(a);
                seleccionados.add(articulo);
                }
            }
        }
        if(obb==0){
            if(tipo==0){
                aar.asignarMasivoRubros(seleccionados,rubroId);
            }else{
                aar.desAsignarMasivoRubros(seleccionados);
            }
        }else{
            //Proveedores
            if(tipo==0){
                aar.asignarMasivoProveedor(seleccionados,rubroId);
            }else{
                aar.desAsignarMasivoProveedor(seleccionados);
            }
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

